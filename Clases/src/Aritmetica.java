public class Aritmetica {
    int numero1;
    int numero2;

    void sumar(){
        var resultado = numero1+numero2;
        System.out.println("EL resultado de la suma es: "+resultado);
    }

    void restar(){
        var resultado = numero1-numero2;
        System.out.println("EL resultado de la resta es: "+resultado);
    }
}
